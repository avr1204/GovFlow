async function submitRecommendation() {
    const t = tr();

    const age = document.getElementById("age").value;
    const income = document.getElementById("income").value;
    const employmentStatus = document.getElementById("employmentStatus").value;
    const preferredLanguage = document.getElementById("preferredLanguage").value;
    const resultBox = document.getElementById("result");

    if (!age || !income || !employmentStatus || !preferredLanguage) {
        resultBox.innerText = t.fillAllFields;
        return;
    }

    const requestData = {
        age: parseInt(age),
        income: parseFloat(income),
        employmentStatus: employmentStatus,
        language: preferredLanguage
    };

    resultBox.innerText = t.loadingRecommendation;

    try {
        const response = await fetch("/api/recommend", {
            method: "POST",
            headers: {
                "Content-Type": "application/json"
            },
            body: JSON.stringify(requestData)
        });

        const data = await response.text();
        resultBox.innerText = data;

        const selectedAid = document.getElementById("selectedAid");
        const formIncome = document.getElementById("formIncome");
        const formEmploymentStatus = document.getElementById("formEmploymentStatus");

        if (selectedAid) selectedAid.value = extractAidName(data);
        if (formIncome) formIncome.value = income;
        if (formEmploymentStatus) formEmploymentStatus.value = employmentStatus;

    } catch (error) {
        resultBox.innerText = t.connectionError;
        console.error(error);
    }
}

function showFillForm() {
    const t = tr();
    const resultText = document.getElementById("result")?.innerText?.trim() || "";

    if (!resultText || resultText === t.defaultResult || resultText === t.loadingRecommendation || resultText === t.connectionError) {
        alert(t.noResultYet);
        return;
    }

    const fillFormSection = document.getElementById("fillFormSection");
    if (fillFormSection) {
        fillFormSection.classList.remove("hidden");
        fillFormSection.scrollIntoView({ behavior: "smooth" });
    }
}

function extractAidName(resultText) {
    const text = resultText.toLowerCase();

    if (text.includes("bantuan sara hidup")) {
        return "Bantuan Sara Hidup";
    } else if (text.includes("student")) {
        return "Student Support";
    } else if (text.includes("elderly") || text.includes("senior")) {
        return "Senior Citizen Aid";
    } else {
        return "Household Assistance";
    }
}

function submitApplicationForm() {
    const t = tr();

    const fullName = document.getElementById("fullName").value;
    const icNumber = document.getElementById("icNumber").value;
    const phoneNumber = document.getElementById("phoneNumber").value;
    const address = document.getElementById("address").value;
    const selectedAid = document.getElementById("selectedAid").value;
    const formIncome = document.getElementById("formIncome").value;
    const formEmploymentStatus = document.getElementById("formEmploymentStatus").value;

    if (!fullName || !icNumber || !phoneNumber || !address || !selectedAid || !formIncome || !formEmploymentStatus) {
        alert(t.fillAllFields);
        return;
    }

    const successBox = document.getElementById("formSuccessMessage");
    if (successBox) {
        successBox.innerText = t.thanksMessage;
        successBox.classList.remove("hidden");
        successBox.scrollIntoView({ behavior: "smooth" });
    }

    const submitBtn = document.getElementById("submitFormBtn");
    if (submitBtn) submitBtn.style.display = "none";
}

async function sendChat() {
    const t = tr();

    const chatInput = document.getElementById("chatInput");
    const chatMessages = document.getElementById("chatMessages");
    const chatLanguage = document.getElementById("chatLanguage").value;
    const message = chatInput.value.trim();

    if (!message) {
        alert(t.typeQuestion);
        return;
    }

    const userBubble = document.createElement("div");
    userBubble.className = "user-message";
    userBubble.innerText = message;
    chatMessages.appendChild(userBubble);

    const botBubble = document.createElement("div");
    botBubble.className = "bot-message";
    botBubble.innerText = t.loadingChat;
    chatMessages.appendChild(botBubble);

    chatMessages.scrollTop = chatMessages.scrollHeight;

    try {
        const response = await fetch("/api/chat", {
            method: "POST",
            headers: {
                "Content-Type": "application/json"
            },
            body: JSON.stringify({
                message: message,
                language: chatLanguage
            })
        });

        const data = await response.text();
        botBubble.innerText = data;
    } catch (error) {
        botBubble.innerText = t.connectionError;
        console.error(error);
    }

    chatInput.value = "";
    chatMessages.scrollTop = chatMessages.scrollHeight;
}

async function getGuide() {
    const t = tr();

    const aidName = document.getElementById("aidName").value.trim();
    const guideLanguage = document.getElementById("guideLanguage").value;
    const guideResult = document.getElementById("guideResult");

    if (!aidName) {
        guideResult.innerText = t.typeAidName;
        return;
    }

    guideResult.innerText = t.loadingGuide;

    try {
        const response = await fetch("/api/guide", {
            method: "POST",
            headers: {
                "Content-Type": "application/json"
            },
            body: JSON.stringify({
                aidName: aidName,
                language: guideLanguage
            })
        });

        const data = await response.text();
        guideResult.innerText = data;
    } catch (error) {
        guideResult.innerText = t.connectionError;
        console.error(error);
    }
}

